# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário com 15% de aumento.
s = float(input('Digite o seu salário em Reais:\nR$'))
a = (s * 15) / 100
print('Seu novo salário, com um aumento de 15%, será R${:.2f}!'.format(s + a))

